    public void fillfataNhacungcap() {
        model = (DefaultTableModel) tblBangNHacc.getModel();
        model.setRowCount(0);
        ArrayList<NhaCungCap_Model> list = (ArrayList<NhaCungCap_Model>) ncc.getallFormNhaCC();
        model.setColumnIdentifiers(new String[]{"Mã Nhà Cung Cấp", "Tên Nhà Cung Cấp", "Địa Chỉ", "Số Điện Thoại",});
        for (NhaCungCap_Model nhaCungCap_Model : list) {
            model.addRow(new Object[]{
                nhaCungCap_Model.getMaNCC(),
                nhaCungCap_Model.getTenNCC(),
                nhaCungCap_Model.getDiaChi(),
                nhaCungCap_Model.getSdt()

            });
        }
        tblBangNHacc.setModel(model);
    }

