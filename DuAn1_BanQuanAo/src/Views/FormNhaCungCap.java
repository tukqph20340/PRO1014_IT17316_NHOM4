    public void mouseclick(List<NhaCungCap_Model> list) {
        int index = tblBangNHacc.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để click");
            return;
        }
        txtMancc.setText(tblBangNHacc.getValueAt(index, 0).toString());
        txtTenNCC.setText(tblBangNHacc.getValueAt(index, 1).toString());
        txtDiaChi.setText(tblBangNHacc.getValueAt(index, 2).toString());
        txtSDT.setText(tblBangNHacc.getValueAt(index, 3).toString());
    }
