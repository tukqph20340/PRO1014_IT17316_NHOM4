    public void mouseclick(List<KieuDang_Model> list) {
        int index = tblBang.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng click chọn 1 dòng");
        }
        txtMaKD.setText(list.get(index).getMaKD());
        txtTenKD.setText(list.get(index).getTenKieuDang());
    }
