    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (check()) {
            if (checkMa(list, txtMaKD.getText())) {
                JOptionPane.showMessageDialog(this, ser.ADDDKieuDang(getDaTa()));
                list = ser.getAllFormKD();
                fillData();
                clear();
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int index = tblBang.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui Lòng chọn dòng để xóa");
            return;
        } else {
            JOptionPane.showMessageDialog(this, ser.UpdateKieuDang(getDaTa()));
            list = ser.getAllFormKD();
            fillData();
            clear();
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int index = tblBang.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui Lòng chọn dòng để xóa");
            return;
        }
        String Ma = txtMaKD.getText();
        JOptionPane.showMessageDialog(this, ser.XoaKieuDang(Ma));
        list = ser.getAllFormKD();
        fillData();
        clear();
    }//GEN-LAST:event_btnXoaActionPerformed

