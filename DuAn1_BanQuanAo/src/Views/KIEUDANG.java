    public void fillData() {
        model.setRowCount(0);
        model = (DefaultTableModel) tblBang.getModel();
        list = ser.getAllFormKD();
        for (KieuDang_Model kieuDang_Model : list) {
            model.addRow(kieuDang_Model.data());
        }
    }

    KieuDang_Model getDaTa() {
        return new KieuDang_Model(txtMaKD.getText(), txtTenKD.getText());
    }

    public boolean check() {
        if (txtMaKD.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã Kiểu Dáng không được để trống");
            return false;
        }
        if (txtTenKD.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên Kiểu Dáng không được để trống");
            return false;
        }
        return true;
    }

    public boolean checkMa(List<KieuDang_Model> lists, String Ma) {
        for (KieuDang_Model kieuDang_Model : lists) {
            if (kieuDang_Model.getMaKD().equalsIgnoreCase(Ma)) {
                JOptionPane.showMessageDialog(this, "Mã Kiểu dáng đã tồn tại! ");
                return false;
            }
        }
        return true;
    }

    public void clear() {
        txtMaKD.setText("");
        txtTenKD.setText("");
    }

