    public void fillData() {
        model.setRowCount(0);
        model = (DefaultTableModel) tblBang.getModel();
        listt = ser.getAllForm();
        for (DanhMucSP_Model danhMucSP_Model : listt) {
            model.addRow(danhMucSP_Model.data());
        }
    }

    DanhMucSP_Model getDaTa() {
        return new DanhMucSP_Model(txtMa.getText(), txtTenDM.getText());
    }

    public boolean check() {
        if (txtMa.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mã danh mục không được để trống");
            return false;
        }
        if (txtTenDM.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Tên danh mục không được để trống");
            return false;
        }
        return true;
    }

    public boolean checkMa(List<DanhMucSP_Model> lists, String Ma) {
        for (DanhMucSP_Model kieuDang_Model : lists) {
            if (kieuDang_Model.getMaDanhMuc().equalsIgnoreCase(Ma)) {
                JOptionPane.showMessageDialog(this, "Mã danh mục đã tồn tại! ");
                return false;
            }
        }
        return true;
    }

    public void clear() {
        txtMa.setText("");
        txtTenDM.setText("");
    }

