    public void mouseclick(List<DanhMucSP_Model> list) {
        int index = tblBang.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng click chọn 1 dòng");
        }
        txtMa.setText(list.get(index).getMaDanhMuc());
        txtTenDM.setText(list.get(index).getTendanhMuc());
    }
